class Autoencoder(nn.Module):
    def __init__(self, input_dim):
        super().__init__()  # Initialize parent class (PyTorch module)

        # Encoder: compresses input data into a smaller representation
        self.encoder = nn.Sequential(
            nn.Linear(input_dim, 16),  # Convert input → 16 features
            nn.ReLU(),                 # Add non-linearity
            nn.Linear(16, 8)           # Reduce to 8 features (compressed data)
        )

        # Decoder: reconstructs original data from compressed form
        self.decoder = nn.Sequential(
            nn.Linear(8, 16),          # Expand 8 → 16
            nn.ReLU(),                 # Activation
            nn.Linear(16, input_dim)   # Restore to original input size
        )

    def forward(self, x):
        # Encode input into compressed representation
        z = self.encoder(x)

        # Decode compressed data back to original form
        return self.decoder(z)

# ===================== DEFINE TREATMENT & OUTCOME =====================
# Treatment variable: Credit Amount (input factor)
treatment = df["Credit_Amount"]

# Outcome variable: Default (target result: 0 or 1)
outcome = df["Default"]


# ===================== CREATE THRESHOLD =====================
# Use median to divide data into two groups
threshold = treatment.median()


# ===================== CREATE TREATED & CONTROL GROUPS =====================
# Treated group: higher credit amount than median
treated = outcome[treatment > threshold]

# Control group: lower or equal credit amount
control = outcome[treatment <= threshold]


# ===================== CALCULATE ATE (Average Treatment Effect) =====================
# Difference in average outcome between treated and control groups
ATE = treated.mean() - control.mean()

print("ATE:", ATE)


# ===================== CALCULATE ITE (Individual Treatment Effect - Approx) =====================
# Difference between individual treated and control values (approximation)
ITE = treated.values[:len(control)] - control.values

print("Sample ITE:", ITE[:10])  # Show first 10 values

