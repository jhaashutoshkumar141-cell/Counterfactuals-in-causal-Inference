# ===================== NEURAL NETWORK (MLP MODEL) =====================

# Define a Multi-Layer Perceptron (MLP) class
class MLP(nn.Module):
    def __init__(self, input_dim):
        super().__init__()

        # Define network layers
        # Input → 32 neurons → 16 neurons → Output (2 classes)
        self.model = nn.Sequential(
            nn.Linear(input_dim, 32),  # Input layer to hidden layer
            nn.ReLU(),                 # Activation function
            nn.Linear(32, 16),         # Hidden layer
            nn.ReLU(),                 # Activation function
            nn.Linear(16, 2)           # Output layer (2 classes: Default / No Default)
        )

    # Forward pass (how data flows through network)
    def forward(self, x):
        return self.model(x)


# Create model instance
mlp = MLP(X_train.shape[1])  # input_dim = number of features


# ===================== LOSS & OPTIMIZER =====================

# Loss function (used for classification problems)
criterion = nn.CrossEntropyLoss()

# Optimizer (updates model weights to reduce loss)
optimizer = optim.Adam(mlp.parameters(), lr=0.001)


# ===================== CONVERT DATA TO TENSORS =====================

# Convert training data to PyTorch tensors
X_train_tensor = torch.FloatTensor(X_train)
y_train_tensor = torch.LongTensor(y_train.values)


# ===================== TRAINING LOOP =====================

# Train model for 50 epochs (iterations)
for epoch in range(50):

    # Clear previous gradients
    optimizer.zero_grad()

    # Forward pass → get predictions
    outputs = mlp(X_train_tensor)

    # Calculate loss (difference between prediction & actual)
    loss = criterion(outputs, y_train_tensor)

    # Backpropagation → compute gradients
    loss.backward()

    # Update weights
    optimizer.step()

    # Print loss every 10 epochs
    if epoch % 10 == 0:
        print(f"Epoch {epoch}, Loss: {loss.item()}")

